package com.TweetAnalyzer.TagAnalyzer.model;

import java.time.LocalDate;

public class Super {
	
	private LocalDate date;
	private long id;
	public Super(LocalDate date, long id) {
		super();
		this.date = date;
		this.id = id;
	}
	public LocalDate getDate() {
		return date;
	}
	
	public long getId() {
		return id;
	}
	
	
	
	
	

}
